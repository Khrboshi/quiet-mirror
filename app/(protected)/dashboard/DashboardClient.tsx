// ── Progress nudge (2–4 entries) ──────────────────────────────────────────────

function ProgressNudge({ entryCount }: { entryCount: number }) {
  const { t } = useTranslation();
  const remaining = 5 - entryCount;
  if (remaining <= 0) return null;

  return (
    <div className="mb-8 rounded-2xl border border-qm-border-subtle bg-qm-elevated p-5">
      <div className="mb-2 flex items-center justify-between">
        <p className="text-[10px] font-semibold uppercase tracking-[0.2em] text-qm-faint">
          {t.dashboard.patternFormingYours}
        </p>
        <p className="text-xs text-qm-faint">
          {entryCount} / 5 entries
        </p>
      </div>
      <div className="mb-3 h-1 w-full rounded-full bg-qm-card">
        <div
          className="h-1 rounded-full bg-qm-positive-muted transition-all"
          style={{ width: `${(entryCount / 5) * 100}%` }}
        />
      </div>
      <p className="text-sm text-qm-muted">
        <span className="text-qm-primary">
          {t.dashboard.moreEntries(remaining)}
        </span>{" "}
        and Quiet Mirror will start showing you what quietly repeats across your
        writing.
      </p>
    </div>
  );
}

// ── Main ──────────────────────────────────────────────────────────────────────

export default function DashboardClient({
   data }: { data: DashboardData }) {
  const { t } = useTranslation();
  const { supabase } = useSupabase();
  const { planType, credits, loading: planLoading } = useUserPlan();

  const [displayName, setDisplayName] = useState<string | null>(null);
  const [mounted, setMounted] = useState(false);
  useEffect(() => { setMounted(true); }, []);

  useEffect(() => {
    supabase.auth.getUser().then(({ data: d }) => {
      setDisplayName(friendlyNameFromUser(d?.user));
    });
  }, [supabase]);

  const isPremium = planType === "PREMIUM" || planType === "TRIAL";
  const isFree = !planLoading && planType === "FREE";
  const reflectionsPaused = isFree && (credits ?? 0) === 0;
  const resetLabel = startOfNextMonth();
  const readablePlan = isPremium
    ? planType === "TRIAL" ? t.dashboard.trial : t.dashboard.premium
    : t.dashboard.free;

  const greeting = displayName
    ? `${greetingByHour(t)}, ${displayName}`
    : greetingByHour(t);

  const prompts = useMemo(() => getDailyPrompts(t.dashboard.prompts), [t.dashboard.prompts]);

  const {
    entryCount, writingDays, lastEntryId, lastEntryTitle, lastEntryDate,
    lastEntryHasReflection, lastTopEmotion, lastTopTheme, lastCorepattern,
    wroteToday, reflectedThisWeek,
  } = data;

  const subline = useMemo(() => {
    if (entryCount === 0) return t.journal.emptyStateNudge;
    if (wroteToday) return t.dashboard.writtenToday;
    return t.dashboard.choosePrompt;
  }, [entryCount, wroteToday]);

  return (
    <div className="mx-auto max-w-5xl px-5 pb-20 pt-10 text-qm-primary sm:px-6">

      {/* ── Header ── */}
      <div className="mb-8">
        <div className="flex flex-wrap items-start justify-between gap-3">
          <div className="space-y-1">
            {/* ✅ Fraunces serif on the greeting — most personal line in the app */}
            <h1 className="font-display text-3xl font-semibold tracking-tight text-qm-primary sm:text-4xl">
              {greeting}
            </h1>
            <p className="text-sm text-qm-faint">{subline}</p>
          </div>
        </div>

        {/* ✅ Replaced debug-style pill badges with a subtle single-line status row */}
        <div className="mt-4 flex flex-wrap items-center gap-x-4 gap-y-1 text-xs text-qm-faint">
          <span>
            Plan:{" "}
            <span className={isPremium ? "text-qm-positive" : "text-qm-muted"}>
              {readablePlan}
            </span>
          </span>

          <span className="text-qm-faint">·</span>

          {isPremium ? (
            <span>
              {t.dashboard.reflUnlimited}
            </span>
          ) : reflectionsPaused ? (
            <span>
              {t.dashboard.reflPaused(resetLabel)}
            </span>
          ) : (
            <span>
              {t.dashboard.reflRemaining}{" "}
              <span className="text-qm-muted">
                {planLoading ? "…" : String(credits ?? 0)}
              </span>
            </span>
          )}

          {entryCount > 0 && (
            <>
              <span className="text-qm-faint">·</span>
              <span>
                {entryCount} {entryCount === 1 ? t.dashboard.entry : t.dashboard.entries}
              </span>
            </>
          )}
        </div>

        {reflectionsPaused && (
          <p className="mt-1.5 text-xs text-qm-faint">
            You can always write freely — reflections return {resetLabel}.
          </p>
        )}

        {/* Bug #23 fix: upgrade CTA at 0-credit wall — highest-intent moment */}
        {reflectionsPaused && !isPremium && (
          <div className="mt-3 flex items-center gap-3">
            <Link
              href="/upgrade"
              className="inline-flex items-center rounded-full bg-qm-accent px-4 py-1.5 text-xs font-semibold text-white hover:bg-qm-accent-hover transition-colors"
            >
              Unlock unlimited reflections →
            </Link>
            <span className="text-xs text-qm-faint">{PRICING.trialDays}-day full refund · Cancel anytime</span>
          </div>
        )}
      </div>

      {/* ── Onboarding moments ── */}
      {entryCount === 0 && <WelcomePanel />}

      {entryCount === 1 && lastEntryHasReflection && (
        <PatternStartedCard emotion={lastTopEmotion} theme={lastTopTheme} />
      )}

      {entryCount >= 2 && entryCount < 5 && (
        <ProgressNudge entryCount={entryCount} />
      )}

      {/* ── Premium insight / Free teaser ── */}
      {isPremium && (lastTopEmotion || lastTopTheme || lastCorepattern) && (
        <div className="mb-8">
          <PremiumInsightCard
            emotion={lastTopEmotion}
            theme={lastTopTheme}
            corepattern={lastCorepattern}
            reflectedThisWeek={reflectedThisWeek}
          />
        </div>
      )}

      {isFree && entryCount >= 5 && (
        <div className="mb-8">
          <FreeInsightTeaser
            entryCount={entryCount}
            emotion={lastTopEmotion}
            theme={lastTopTheme}
          />
        </div>
      )}

      {/* ── Daily prompts ── */}
      <div className="mb-6">
        <p className="mb-3 text-[10px] font-semibold uppercase tracking-[0.2em] text-qm-faint">
          Today&apos;s prompts
        </p>

        <div className="grid gap-3 sm:grid-cols-3">
          {prompts.map((c) => {
            const ac = ACCENT_CLASSES[c.accent] ?? ACCENT_CLASSES.slate;
            return (
              <Link
                key={c.q}
                href={buildHref(c.q)}
                className={`group rounded-2xl border bg-white/[0.02] p-5 transition hover:bg-white/[0.05] ${ac.border}`}
              >
                {/* ✅ Colored dot per-prompt for visual differentiation */}
                <span className={`mb-3 block h-1.5 w-1.5 rounded-full ${ac.dot}`} />
                <p className="font-medium leading-snug text-qm-primary transition group-hover:text-qm-primary">
                  {c.q}
                </p>
                <p className="mt-1.5 text-xs leading-relaxed text-qm-faint">{c.sub}</p>
                <p className="mt-3 text-xs font-medium transition text-qm-positive group-hover:text-qm-positive-hover">
                  Start →
                </p>
              </Link>
            );
          })}
        </div>
      </div>

      {/* ── Thread + history row ── */}
      <div className="grid gap-4 sm:grid-cols-2">

        <ThreadCard
          lastEntryId={lastEntryId}
          lastEntryTitle={lastEntryTitle}
          lastEntryDate={lastEntryDate}
          lastTopEmotion={lastTopEmotion}
          wroteToday={wroteToday}
        />

        {/* ✅ History card — cleaner, less "settings panel" feel */}
        <div className="rounded-2xl border border-white/[0.08] bg-white/[0.03] p-5">
          <p className="text-[10px] font-semibold uppercase tracking-[0.2em] text-qm-faint">
            Your history
          </p>

          <div className="mt-4 space-y-2.5">
            <div className="flex items-center justify-between text-sm">
              <span className="text-qm-faint">{t.dashboard.totalEntries}</span>
              <span className="font-medium text-qm-primary">{entryCount}</span>
            </div>
            <div className="flex items-center justify-between text-sm">
              <span className="text-qm-faint">{t.dashboard.writingDays}</span>
              <span className="font-medium text-qm-primary">{writingDays}</span>
            </div>
            {lastEntryDate && (
              <div className="flex items-center justify-between text-sm">
                <span className="text-qm-faint">{t.dashboard.lastEntryLabel}</span>
                <span className="font-medium text-qm-primary" suppressHydrationWarning>
                  {friendlyDate(lastEntryDate, mounted, t)}
                </span>
              </div>
            )}
            {isPremium && (
              <div className="flex items-center justify-between text-sm">
                <span className="text-qm-faint">{t.dashboard.reflLabel}</span>
                <span className="font-medium text-qm-positive">{t.dashboard.reflUnlimitedShort}</span>
              </div>
            )}
          </div>

          <div className="mt-5 flex flex-wrap gap-3 border-t border-white/[0.05] pt-4">
            <Link
              href="/journal"
              className="text-xs text-qm-faint transition hover:text-qm-secondary"
            >
              All entries →
            </Link>
            {isPremium && (
              <Link
                href="/insights"
                className="text-xs text-qm-positive transition hover:text-qm-positive-hover"
              >
                View insights →
              </Link>
            )}
          </div>
        </div>
      </div>

    </div>
  );
}
