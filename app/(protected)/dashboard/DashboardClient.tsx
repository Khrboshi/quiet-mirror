// ── Premium insight card ──────────────────────────────────────────────────────

function PremiumInsightCard({
  emotion,
  theme,
  corepattern,
  reflectedThisWeek,
}: {
  emotion: string | null;
  theme: string | null;
  corepattern: string | null;
  reflectedThisWeek: boolean;
}) {
  const safePattern = sanitiseCorePattern(corepattern);
  const hasData = emotion || theme || safePattern;
  if (!hasData) return null;

  const bodyText = safePattern
    ? safePattern
    : emotion && theme
    ? `${emotion} keeps showing up — often alongside themes of ${theme.toLowerCase()}.`
    : emotion
    ? `${emotion} has been your most present emotion recently.`
    : `${theme} is the theme appearing most in your entries.`;

  return (
    <div className="rounded-2xl border border-qm-positive-border bg-gradient-to-br from-qm-positive-strong/[0.06] to-transparent p-5 sm:p-6">
      <p className="mb-3 text-[10px] font-semibold uppercase tracking-[0.2em] text-qm-positive">
        Your pattern right now
      </p>

      <p className="font-display text-base font-medium leading-relaxed text-qm-primary sm:text-[17px]">
        &ldquo;{bodyText}&rdquo;
      </p>

      {(emotion || theme) && (
        <div className="mt-3 flex flex-wrap gap-2">
          {emotion && (
            <span className="rounded-full border border-qm-premium-border bg-qm-premium-soft px-2.5 py-0.5 text-xs text-qm-premium">
              {emotion}
            </span>
          )}
          {theme && (
            <span className="rounded-full border border-qm-positive-border bg-qm-positive-soft px-2.5 py-0.5 text-xs text-qm-positive">
              {theme}
            </span>
          )}
        </div>
      )}

      <div className="mt-4 flex flex-wrap items-center gap-3">
        <Link
          href="/insights"
          className="text-xs font-medium text-qm-positive transition hover:text-qm-positive-hover"
        >
          See full insights →
        </Link>
        {!reflectedThisWeek && (
          <>
            <span className="text-xs text-qm-faint">·</span>
            <Link
              href="/journal"
              className="text-xs text-qm-faint transition hover:text-qm-muted"
            >
              Reflect on a recent entry
            </Link>
          </>
        )}
      </div>
    </div>
  );
}

// ── Free insight teaser ───────────────────────────────────────────────────────

function FreeInsightTeaser({
  entryCount,
  emotion,
  theme,
}: {
  entryCount: number;
  emotion: string | null;
  theme: string | null;
}) {
  const { t } = useTranslation();
  const hasHint = emotion || theme;

  if (entryCount < 3) {
    return (
      <div className="rounded-2xl border border-white/[0.08] bg-white/[0.03] p-5 sm:p-6">
        <p className="mb-2 text-[10px] font-semibold uppercase tracking-[0.2em] text-qm-faint">
          Your patterns
        </p>
        <p className="text-sm leading-relaxed text-qm-muted">
          Write{" "}
          <span className="text-qm-primary">
            {t.dashboard.moreEntries(3 - entryCount)}
          </span>{" "}
          and Quiet Mirror will start noticing what quietly repeats across your writing.
        </p>
      </div>
    );
  }

  return (
    <div className="rounded-2xl border border-white/[0.08] bg-white/[0.03] p-5 sm:p-6">
      <p className="mb-3 text-[10px] font-semibold uppercase tracking-[0.2em] text-qm-faint">
        {t.dashboard.patternForming}
      </p>

      {hasHint ? (
        <p className="text-sm leading-relaxed text-qm-secondary">
          {t.dashboard.patternTeaser.split("{theme}")[0]}
          <span className="select-none rounded bg-white/10 px-1 text-qm-primary blur-[5px]">
            {emotion ?? theme}
          </span>
          {t.dashboard.patternTeaser.split("{theme}")[1]}
        </p>
      ) : (
        <p className="text-sm leading-relaxed text-qm-secondary">
          {t.dashboard.patternTeaserNoHint}
        </p>
      )}

      <Link
        href="/upgrade"
        className="mt-4 inline-flex items-center gap-1.5 rounded-full bg-qm-accent px-4 py-2 text-xs font-semibold text-white transition hover:bg-qm-accent-hover"
      >
        {t.dashboard.unlockInsights}
      </Link>
    </div>
  );
}

// ── Thread card ───────────────────────────────────────────────────────────────

function ThreadCard({
  lastEntryId,
  lastEntryTitle,
  lastEntryDate,
  lastTopEmotion,
  wroteToday,
}: {
  lastEntryId: string | null;
  lastEntryTitle: string | null;
  lastEntryDate: string | null;
  lastTopEmotion: string | null;
  wroteToday: boolean;
}) {
  const { t } = useTranslation();
  const [mounted, setMounted] = useState(false);
  useEffect(() => { setMounted(true); }, []);
  const when = lastEntryDate ? friendlyDate(lastEntryDate, mounted, t) : null;
  const title = titleOrUntitled(lastEntryTitle, t.journal.untitledEntry);

  const sectionLabel = wroteToday
    ? t.dashboard.wroteToday
    : lastEntryId
    ? t.dashboard.pickUpThread
    : t.dashboard.startHere;

  // Don't echo the raw entry title into the prompt body — feels broken with test titles
  const body = wroteToday
    ? t.dashboard.dayEvolved
    : lastEntryId && lastTopEmotion
    ? `You wrote ${when}. ${lastTopEmotion} was present. Has anything shifted?`
    : lastEntryId
    ? `You last wrote ${when}. Has anything softened since?`
    : t.dashboard.oneHonestSentence;

  const threadPrompt = wroteToday
    ? t.dashboard.alreadyWroteToday
    : lastTopEmotion
    ? `Following up on your last entry — ${lastTopEmotion} was present. Has anything shifted?`
    : `Following up on your last entry — has anything softened since you wrote?`;

  return (
    <div className="rounded-2xl border border-white/[0.08] bg-white/[0.03] p-5">
      <p className="text-[10px] font-semibold uppercase tracking-[0.2em] text-qm-faint">
        {sectionLabel}
      </p>
      <p className="mt-2 text-sm font-medium leading-snug text-qm-primary">
        {body}
      </p>

      <div className="mt-4 flex flex-wrap gap-2">
        <Link
          href={buildHref(threadPrompt)}
          className="inline-flex rounded-full bg-qm-accent px-4 py-2 text-sm font-semibold text-white shadow-sm transition hover:bg-qm-accent-hover"
        >
          {wroteToday ? t.dashboard.addToToday : t.dashboard.writeNow}
        </Link>

        {lastEntryId && (
          <Link
            href={`/journal/${lastEntryId}`}
            className="inline-flex rounded-full border border-white/[0.08] bg-white/[0.03] px-4 py-2 text-sm font-medium text-qm-secondary transition hover:bg-white/[0.07]"
          >
            {t.dashboard.openLastEntry}
          </Link>
        )}
      </div>
    </div>
  );
}

